CREATE Procedure [dbo].[GravarVoo]
@VooId int,
@LocalPartida nvarchar(30),
@LocalChegada nvarchar(30),
@DataPartida datetime,
@DataChegada datetime,
@Lotacao int,
@Capacidade int,
@Estado int,
@CompanhiaId int
As 
Begin

Begin Transaction
   
 IF (Select Count(*) From Voo Where VooId=@VooId)=0
      Begin
         Insert Into [Voo] (VooId, LocalPartida, LocalChegada, DataPartida, DataChegada, Lotacao, Capacidade, Estado, CompanhiaId)
         Values (@VooId, @LocalPartida, @LocalChegada, @DataPartida, @DataChegada, @Lotacao, @Capacidade, @Estado, @CompanhiaId)
      End
 Else
     Begin
         Update Voo
         Set LocalPartida = @LocalPartida, LocalChegada=@LocalChegada, DataPartida=@DataPartida, DataChegada=@DataChegada, Lotacao=@Lotacao, Capacidade=@Capacidade, Estado=@Estado, CompanhiaId=@CompanhiaId
         Where VooId=@VooId
      End
 
      If @@error <>0
            Begin
               Rollback transaction
            End
      Else
            Begin
               Commit Transaction
            End
End

GO

